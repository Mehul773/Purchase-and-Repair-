const registerDean = async (req, res) => {
  try {
    const { name, email, password } = req.body;

    const userExists = await Dean.findOne({ email });

    if (userExists) {
      res.json({ message: "User Already Exists" });
    }

    // Hash Password

    const salt = await bcrypt.genSalt(10);
    const hashedPassword = await bcrypt.hash(password, salt);

    //Create User

    const user = await Dean.create({
      name,
      email,
      password: hashedPassword,
      status: "Pending",
    });

    if (user) {
      const token = await user.generateAuthToken();
      console.log(`Generated by signup ${token}`);

      res.cookie("jwtokendean", token, {
        expires: new Date(Date.now() + 86400000),
      });

      await user.save();

      res.status(201).json({
        _id: user.id,
        name: user.name,
        email: user.email,
        token: token,
        message: "Successfully signed up",
      });
    } else {
      res.status(400);
    }
  } catch (error) {
    console.log(error);
  }
};

const getAllPending = async (req, res) => {
  try {
    const { status } = req.body;

    const dean = await Dean.find({ status });
    if (!dean) {
      res.json({ message: "No pending Dean" });
    } else {
      res.json({ message: "Dean Details", dean: dean });
    }
  } catch (error) {
    console.log(error);
  }
};

const makeActive = async (req, res) => {
  try {
    const { email } = req.body;

    Dean.findOne({ email }, (err, dean) => {
      if (err) return res.status(500).send(err);

      if (dean.status === "Pending") {
        dean.status = "Active";
      }

      dean.save((err, pcs) => {
        if (err) return res.status(500).send(err);
        res.send(pcs);
      });
    });
  } catch (error) {
    console.log(error);
  }
};
